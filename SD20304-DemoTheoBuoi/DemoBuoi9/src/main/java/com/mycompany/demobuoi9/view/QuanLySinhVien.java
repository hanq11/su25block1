    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        int selectedIndex = tblSinhVien.getSelectedRow();
        SinhVien svDuocChon = sinhVienRepository.getAll().get(selectedIndex);
        txtId.setText(svDuocChon.getId().toString());
        txtTen.setText(svDuocChon.getTen());
        txtTuoi.setText(svDuocChon.getTuoi().toString());
        if(svDuocChon.getGioiTinh()) {
            rdoNu.setSelected(true);
        } else {
            rdoNam.setSelected(true);
        }
    }//GEN-LAST:event_tblSinhVienMouseClicked

