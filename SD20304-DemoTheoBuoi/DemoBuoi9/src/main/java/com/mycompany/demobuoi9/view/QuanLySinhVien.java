    public void fillTableData(List<SinhVien> danhSach) {
        DefaultTableModel dtm = (DefaultTableModel) tblSinhVien.getModel();
        dtm.setRowCount(0);
        for(SinhVien sv: danhSach) {
            dtm.addRow(new Object[] {
                sv.getId(),
                sv.getTen(),
                sv.getTuoi(),
                sv.getGioiTinh() == true ? "Nu" : "Nam"
            });
        }
    } 
    
    public SinhVien getFormData() {
        return new SinhVien(
                Integer.valueOf(txtId.getText().isBlank() ? "0" : txtId.getText()),
                txtTen.getText(),
                Integer.valueOf(txtTuoi.getText()),
                rdoNam.isSelected() == true ? false : true
        );
    }
